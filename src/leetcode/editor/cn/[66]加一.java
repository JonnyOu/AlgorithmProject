class Solution66 {


    public static void main(String[] args) {
        int[] nums = {9,9,8};
        System.out.println(new Solution66().plusOne(nums));
    }
    
    public int[] plusOne(int[] digits) {
        int len = digits.length, flag = 1;
        for (int i = len-1; i >= 0; i--) {
            int num = digits[i] + 1;
            if (num == 10) {
                digits[i] = 0;
                flag = 1;
            } else {
                digits[i] = num;
                flag = 0;
                break;
            }
        }
        if (flag == 1) {
            int[] arr = new int[len+1];
            arr[0] = 1;
            return arr;
        } else {
            return digits;
        }
    }
}
